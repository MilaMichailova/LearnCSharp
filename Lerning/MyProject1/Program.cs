Console.WriteLine(" Программа, которая вычисляет факториал числа с проверкой ввода\n");


// выводим приветственное сообщение
Console.WriteLine("Вычисление факториала числа (поддержка больших чисел).");

//  цикл пока для повторного ввода чисел
while (true)
{
    // просим ввести число или команду выхода
    Console.WriteLine("Введите целое неотрицательное число (или 'exit' для выхода):");
    string input = Console.ReadLine().Trim(); // считываем и обрезаем пробелы

    // проверяем, хочет ли пользователь выйти из программы
    if (input.Equals("exit", StringComparison.OrdinalIgnoreCase))
    {
        Console.WriteLine("Выход из программы.");
        break; // прерываем цикл и завершаем программу
    }

    try
    {
        // пытаемся преобразовать введённую строку в целое число
        int n = int.Parse(input);

        // проверяем, что число неотрицательное
        if (n < 0)
        {
            Console.WriteLine("Ошибка: число должно быть неотрицательным.");
            continue; //переходим к следующей итерации цикла (новый ввод)
        }

        // вычисляем факториал с помощью метода Factorial
        BigInteger fact = Factorial(n);

        //выводим результат вычисления
        Console.WriteLine($"Факториал числа {n} равен:\n{fact}");
    }
    catch (FormatException)
    {
        //обработка ошибки: введена не числовая строка
        Console.WriteLine("Ошибка: введено не целое число.");
    }
    catch (OverflowException)
    {
        //обработка ошибки: введённое число слишком большое для int
        Console.WriteLine("Ошибка: число слишком большое для типа int.");
    }
    catch (Exception ex)
    {
        // обработка всех остальных неожиданных ошибок
        Console.WriteLine($"Неожиданная ошибка: {ex.Message}");
    }

    // спрашиваем пользователя, хочет ли он вычислить ещё один факториал
    Console.WriteLine("Хотите вычислить факториал другого числа? едите (да/нет/y/) для продолжения, или введите любой другой символ для выхода");
    string answer = Console.ReadLine().Trim().ToLower();

    // если ответ не положительный, выходим из программы
    if (answer != "да" && answer != "yes" && answer != "y")
    {
        Console.WriteLine("Выход из программы.");
        break;
    }
    Console.WriteLine();
}

// метод для вычисления факториала числа n
static BigInteger Factorial(int n)
{
    BigInteger result = BigInteger.One; // инициализируем результат 1

    // перемножаем все числа от 2 до n включительно
    for (int i = 2; i <= n; i++)
    {
        result *= i;
    }

    return result; // возвращаем вычисленный факториал
}
